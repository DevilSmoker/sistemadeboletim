    private void bbtnCadastrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_bbtnCadastrarActionPerformed
    {//GEN-HEADEREND:event_bbtnCadastrarActionPerformed
        if(txtDataAula.getText().isEmpty() || txtDataAula.getText().equalsIgnoreCase("  /  /    "))
        {
            JOptionPane.showMessageDialog(this, "Data da aula n√£o pode ser vazio!");
        }
        else
        {
            try
            {
               Aula aula = new Aula();
               aula.setDtAula(new SimpleDateFormat("dd/MM/yyyy").parse(txtDataAula.getText()));
               aula.setMateria((Materia)cbDisciplina.getSelectedItem());
               aula.setProfessor((Professor) cbProfessor.getSelectedItem());
               
               if(new AulaDAO().salvarAula(aula))
               {
                   if(JOptionPane.showConfirmDialog(this, "Aula cadastrada com sucesso! Deseja realizar a Chamada?","Fazer chamada",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
                   {
                       Chamada ch = new Chamada(this, aula);
                       ch.setVisible(true);
                   }
               }
               else
               {
                   JOptionPane.showMessageDialog(this, "Erro ao cadastrar a aula");
               }
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_bbtnCadastrarActionPerformed

