    public void CarregarProfessor()
    {
        try
        {
            for(Professor p : new ProfessorDAO().todosProfessores())
            {
                cbProfessor.addItem(p);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }
    
    public void CarregarDisciplina()
    {
        try
        {
            for(Materia d : new MateriaDAO().todasMaterias())
            {
                cbDisciplina.addItem(d);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }
    
    
