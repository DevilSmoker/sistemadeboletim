    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSalvarActionPerformed
    {//GEN-HEADEREND:event_btnSalvarActionPerformed
        if(txtDescricao.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Campo disciplina n√£o pode ser vazio!");
        }
        else
        {
            try
            {
                if(new MateriaDAO().salvarMateria(new Materia(0,txtDescricao.getText())))
                {
                    JOptionPane.showMessageDialog(this, "Disciplina salva com sucesso!");

                    Limpar();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar a Disciplina!");
                }
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
            }

            CarregaGridMaterias();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

