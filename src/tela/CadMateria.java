    public void CarregaGridMaterias()
    {
        DefaultTableModel model = (DefaultTableModel) gdvMateria.getModel();
        model.setNumRows(0);
        
        try
        {
            for(Materia a : new MateriaDAO().todasMaterias())
            {
                model.addRow(new Object[]{a.getId(),a.getMateria()});
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Erro ao carregar as Disciplinas!");
        }
        
    }
    
