    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSalvarActionPerformed
    {//GEN-HEADEREND:event_btnSalvarActionPerformed
        if(txtNome.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Campo nome n√£o pode ser vazio!");
        }
        else
        {
            try
            {
                Professor p = new Professor();
                p.setNome(txtNome.getText());
                
                if(new ProfessorDAO().salvarProfessor(p))
                {
                    JOptionPane.showMessageDialog(this, "Professor salvo com sucesso!");
                    
                    Limpar();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar o professor!");
                }
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
            }

            CarregaGridProfessor();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

