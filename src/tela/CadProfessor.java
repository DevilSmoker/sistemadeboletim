    public void CarregaGridProfessor()
    {
        DefaultTableModel model = (DefaultTableModel) gdvProfessor.getModel();
        model.setNumRows(0);
        
        try
        {
            for(Professor p : new ProfessorDAO().todosProfessores())
            {
                model.addRow(new Object[]{p.getId(),p.getNome()});
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Erro ao carregar os professores!");
        }
    }
    
    public void Limpar()
    {
        txtNome.setText("");
    }
    
    
